console.log("OPERADORES ARITMÉTICOS");
// igual
console.log(1 === 1);
// diferente
console.log(1 != 1);
// maior
console.log(1 > 1);
// menor
console.log(1 < 1);
// maior ou igual
console.log(1 >= 1);
// menor ou igual
console.log(1 <= 1);


console.log("\n");
console.log("OPERADORES LÓGICOS");
// operadores logicos
// e
console.log(true && true);
console.log(true && false);
// ou
console.log(true || true);
console.log(true || false);
// nao
console.log(!true);
console.log(!false);